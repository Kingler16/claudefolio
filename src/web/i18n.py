    """Return the full translation dict for the given language.

    Falls back to German if the requested language is not available.

    >>> t = get_translations("en")
    >>> t["nav"]["dashboard"]
    'Dashboard'
    >>> t["analyse"]["months"][4]
    'May'
    """
    return _TRANSLATIONS.get(lang, _TRANSLATIONS["de"])
